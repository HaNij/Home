public class PrimitiveType {
//    Логический тип
    boolean aBoolean = true;

//    Символьный тип
    char aChar = 'h';

//    Целочисленные типы (с фиксированной точкой)
    byte aByte = 23; // 2^8 - 1
    short aShort = 500; // 2^16 - 1
    int anInt = 1200; // 2^32 - 1
    long aLong = 50000; // 2^64

//    Вещественные типы (с плавающей точкой)
    float aFloat = 2.2f;
    double aDouble = 50.4d;
}
